package com.pkinc.begusaraisabji;

public class Fruit {
    private String name;
    private String price;

    public Fruit() {
        this.name = "";
        this.price = "";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
